                    <h2>Le questionnaire SUS?</h2>
                    <p>
                        Nous avons l’envie d’utiliser le questionnaire SUS (System Usability Scale) qui permet d’analyser l’utilisabilité d’une page ou d’un pacours de façon très simple. <br><br>
                        Testapic nous avait proposé le test Attrakdif qui cible les points suivant : l'attractivité, l'utilisabilité, la désirabilité, l'ergonomie et l'accessibilité.
                        Cependant ce test était plus complexe à analyser et la désirabilité et attractivité n’était pas dans nos besoins. <br><br>
                        Dans un soucis d’amélioration continue, nous souhaitons un test reutilisable pour d’autre tunnel de devis similaire à celui-ci, afin de comparer les resultats entre les diffferentes versions.
                        En effet ce nouveau tunnel à pour but d’être décliné sur d’autres parcours (habitation/epargne, etc...) <br><br>
                        Le questionnaire SUS est composé de dix questions interverti entre positif et négatif. Pour chaque question l’utilisateur à la possibilité de répondre entre cinq réponses allant de “Pas dutout d’accord” à “Tout à fait d’accord”. <br><br>
                        En plus du questionnaire, nous avons laissé la possibilité aux testeur de nous écrire ce qu’il avait le plus et le moins apprécié, ainsi que les axes d’améliorations possible.
